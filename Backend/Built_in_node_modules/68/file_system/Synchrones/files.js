const fs=require('fs');

//it is an synchrones call
//creating a file with text
fs.writeFileSync("./text.txt","Hello Sara");

//reading a file
// synchrones
const reading=fs.readFileSync("./reading.txt","utf-8");
console.log(reading);

//appending  file
fs.appendFileSync("./text.txt","\nHey yor are appending the text here!!"); 


//copying a file
fs.cpSync("./text.txt","./copy.txt");


//deleting a file
try{
    fs.unlinkSync("./text.txt");
}catch(e){
    console.log("Error in deleting file",e);
}
