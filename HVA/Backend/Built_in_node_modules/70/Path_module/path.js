//built in path module 
const path=require("node:path");

console.log(__filename,"file name");
console.log(__dirname,"diretory name");

//base name for last portion of path
console.log(path.basename(__filename));
console.log(path.basename(__dirname));

//extension
console.log(path.extname(__filename));
console.log(path.extname(__dirname));

//parse gives objects and properties of it
console.log(path.parse(__filename));
console.log(path.parse(__dirname));

//changing to original path string
console.log(path.format(path.parse(__filename)));
console.log(path.format(path.parse(__dirname)));

//checks wether it an absolute path or not
console.log(path.isAbsolute(__filename));
//relative path
console.log(path.isAbsolute("./data.json"));

//join
console.log(path.join("H","V","A"));
console.log(path.join("/H","//V","A"));
console.log(path.join("H","V","../A"));
console.log(path.join(__dirname,"data.json"));

//resolve
console.log(path.resolve("H","V","A"));
console.log(path.resolve("/H","//V","A"));
console.log(path.resolve("H","V","../A"));
console.log(path.resolve(__dirname,"data.json"));
