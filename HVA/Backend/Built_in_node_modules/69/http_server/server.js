//server creation

//buitl in module
const http= require("http");

//calling the function when ever the client requested 
const server=http.createServer((req,res)=>{

    console.log("Hello this a new server....");

    //sending response to the client request
    res.end("Server successfully created..."); 
});

//listening to the port 3000 for incoming requests
server.listen(3000,()=>{
    //Msg to display on server side.
    console.log("Server is running on port 3000");
});
